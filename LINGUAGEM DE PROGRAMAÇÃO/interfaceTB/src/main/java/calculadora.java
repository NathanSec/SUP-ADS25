    private void igualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_igualActionPerformed
        //armazena o valor do segundo nÃºmero
        n2 = Double.parseDouble(res.getText());
        double resultado = 0;
        //switch que executa a operaÃ§Ã£o com base no operador
        switch (operador){
            case "+":
                resultado = n1 + n2;
                break; 
            case "-":
                resultado = n1 - n2;
                break;
            case "x":
                resultado = n1 * n2;
                break;
            case "/":
                if(n2 == 0){
                    JOptionPane.showMessageDialog(null, "NÃ£o Ã© possÃ­vel dividir por 0.", "Erro", JOptionPane.ERROR_MESSAGE);
                    break;
                }else{
                    resultado = n1 / n2;
                    break;
                }

            case "%":
                resultado = (n1/100)*n2;
                break;           
        }
        res.setText(String.valueOf(resultado));
        
        ArrayList<String>hist = new ArrayList();
    
        hist.add(n1 + " " + operador +n2+"="+resultado );
        historico.setText(String.join("\n", hist));
    }//GEN-LAST:event_igualActionPerformed

